"""
Shared utilities for selfmanaged E2E tests (Flyte 2 "function" style).

Cloud-agnostic pieces live here: base dataclasses, the shared ``env`` used for
cloud-agnostic tasks, the bootstrap env for local-to-remote credential
plumbing, and all the @flyte.trace helpers that wrap uctl / helm / kubectl /
control-plane calls. Cloud-specific scripts (selfmanaged_aws_e2e.py,
selfmanaged_gcp_e2e.py) contribute their own TaskEnvironment with the cloud
CLIs layered onto ``base_image()``.
"""

