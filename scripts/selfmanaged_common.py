def base_image() -> flyte.Image:
    """Debian + helm + kubectl + yq + uctl + uv + flyte/flyteplugins-union."""
    return (
        flyte.Image.from_debian_base()
        .with_apt_packages(
            "curl", "ca-certificates", "git", "jq", "unzip", "gnupg", "lsb-release",
        )
        .with_pip_packages("flyte", "flyteplugins-union", "keyring", "fastapi")
        .with_commands([
            # Helm
            f"curl -fsSL https://get.helm.sh/helm-v{_HELM_VERSION}-linux-amd64.tar.gz"
            f" | tar xz -C /tmp && mv /tmp/linux-amd64/helm /usr/local/bin/ && rm -rf /tmp/linux-amd64",
            # kubectl
            f"curl -fsSLo /usr/local/bin/kubectl"
            f" https://dl.k8s.io/release/v{_KUBECTL_VERSION}/bin/linux/amd64/kubectl"
            f" && chmod +x /usr/local/bin/kubectl",
            # yq
            f"curl -fsSLo /usr/local/bin/yq"
            f" https://github.com/mikefarah/yq/releases/download/v{_YQ_VERSION}/yq_linux_amd64"
            f" && chmod +x /usr/local/bin/yq",
            # uctl
            f"curl -fsSL https://github.com/unionai/uctl/releases/download/v{_UCTL_VERSION}/uctl_Linux_x86_64.tar.gz"
            f" | tar xz -C /usr/local/bin uctl",
            # uv (for the flyte CLI's in-image venvs during validation)
            "curl -fsSL https://astral.sh/uv/install.sh | sh"
            " && mv /root/.local/bin/uv /usr/local/bin/",
        ])
    )


# ============================================================================
# Shell — the one primitive that actually runs commands. Async so the event
# loop stays free; traced so every invocation becomes a checkpoint that won't
# re-run on task retry once it succeeds.
# ============================================================================


@flyte.trace
async def sh(cmd: str, *, check: bool = True) -> str:
    """Run a shell command. Always captures and returns stdout as a string.

    stdout and stderr are logged live so callers that don't use the return
    value still get visible progress. Pass ``check=False`` to tolerate a
    non-zero exit code (returns stdout regardless; errors go to the log).
    """
    logger.info(f"  $ {cmd}")
    proc = await asyncio.create_subprocess_shell(
        cmd,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
    )
    out_b, err_b = await proc.communicate()
    out = (out_b or b"").decode(errors="replace")
    err = (err_b or b"").decode(errors="replace")
    if out.strip():
        logger.info(out.rstrip())
    if err.strip():
        logger.warning(err.rstrip())
    if check and proc.returncode != 0:
        raise RuntimeError(f"Command failed (exit {proc.returncode}): {cmd}\n{err}")
    return out


@flyte.trace
async def sh_ok(cmd: str) -> bool:
    """True iff the command succeeds. Never raises."""
    proc = await asyncio.create_subprocess_shell(
        cmd,
        stdout=asyncio.subprocess.DEVNULL,
        stderr=asyncio.subprocess.DEVNULL,
    )
    await proc.communicate()
    return proc.returncode == 0


