@flyte.trace
async def cluster_wait_healthy(cfg: BaseConfig) -> str:
    """Poll until cluster is enabled + healthy. Returns the cluster's org name.

    Plain ``asyncio.sleep`` is correct here: a @flyte.trace memoizes its
    successful return, so a retry of the enclosing task replays this result
    without re-polling.
    """
    await init_union_client(cfg)
    say(f"cluster_wait_healthy: polling Cluster.get(name={cfg.cluster_name}) "
        f"(timeout {cfg.cluster_healthy_timeout}s)")
    deadline = time.time() + cfg.cluster_healthy_timeout
    while time.time() < deadline:
        try:
            cluster = await Cluster.get.aio(name=cfg.cluster_name)
        except Exception as e:
            say(f"cluster_wait_healthy: Cluster.get failed ({e}); retrying in 15s")
            await asyncio.sleep(15)
            continue
        if cluster.state == "enabled" and cluster.health == "healthy":
            say(f"cluster_wait_healthy: {cfg.cluster_name} is enabled+healthy (org={cluster.organization})")
            return cluster.organization or ""
        say(f"cluster_wait_healthy: state={cluster.state} health={cluster.health}, waiting 15s")
        await asyncio.sleep(15)
    raise RuntimeError(
        f"Cluster {cfg.cluster_name} did not become healthy within "
        f"{cfg.cluster_healthy_timeout}s"
    )


@flyte.trace
async def detect_org_name(cfg: BaseConfig) -> str:
    """Best-effort org lookup via Cluster.get. Returns "" on failure."""
    await init_union_client(cfg)
    try:
        cluster = await Cluster.get.aio(name=cfg.cluster_name)
        return cluster.organization or ""
    except Exception as e:
        logger.info(f"  Cluster.get failed in detect_org_name: {e}")
        return ""


