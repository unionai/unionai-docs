logger = logging.getLogger("flyte.e2e")


def say(msg: str) -> None:
    """Visible progress marker. Goes to stdout, bypasses the Flyte log
    router so it shows up in ``flyte run --local`` output even when the user
    has redirected stdout to a file."""
    print(f"[e2e] {msg}", flush=True)
    logger.info(msg)

