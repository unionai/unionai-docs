# Bootstrap — move local credentials into a Flyte Secret for the remote path.
# ============================================================================

bootstrap_env = flyte.TaskEnvironment(name="selfmanaged-bootstrap")


def _read_local_credentials() -> dict[str, str]:
    """Read AWS STS creds + UNION_API_KEY from the local shell."""
    creds = {
        "AKID": os.environ.get("AWS_ACCESS_KEY_ID", ""),
        "SAK":  os.environ.get("AWS_SECRET_ACCESS_KEY", ""),
        "TOK":  os.environ.get("AWS_SESSION_TOKEN", ""),
        "UAK":  os.environ.get("UNION_API_KEY", ""),
    }
    missing = [k for k in ("AKID", "SAK") if not creds[k]]
    if missing:
        raise RuntimeError(
            f"Missing AWS credentials in env: {missing}. "
            "Export AWS_ACCESS_KEY_ID / AWS_SECRET_ACCESS_KEY first."
        )
    return creds


@bootstrap_env.task
async def stash_credentials() -> str:
    """Stash AWS + Union creds from the local shell into a Flyte Secret.

    Run once per shell session (creds rotate with STS tokens). The remote
    ``main`` reads the secret via the env's ``secrets=`` declaration; no creds
    pass through task arguments.
    """
    creds = _read_local_credentials()
    blob = base64.b64encode(json.dumps(creds).encode()).decode()
    flyte.init_from_config()
    Secret.create(name=E2E_CREDS_SECRET_KEY, value=blob, type="regular")
    logger.info(
        f"Secret '{E2E_CREDS_SECRET_KEY}' created ({len(blob)} chars). "
        f"Remote tasks will see it as env var ${E2E_CREDS_ENV_VAR}."
    )
    return E2E_CREDS_SECRET_KEY


async def init_union_client(cfg: "BaseConfig", org: str = "") -> None:
    """Initialize the flyte / flyteplugins-union control-plane client.

    Required before calling ``Cluster.get``, ``ApiKey.create``,
    ``Project.create``, or ``flyte.run.aio`` — they all call
    ``ensure_client()`` under the hood and raise InitializationError if
    nothing has set up the client yet. The SDK's SelectCluster RPC (used for
    code-bundle uploads) also needs ``org`` to route correctly; pass it
    once you know it.

    Always pins endpoint explicitly (ignores ~/.flyte/config.yaml). With
    ``UNION_API_KEY`` set we authenticate via api key; otherwise default
    PKCE pulls a cached token from the keyring.

    Uses the ``.aio`` variants because the sync wrappers raise
    "Deadlock detected" when invoked from an async task. Idempotent.
    """
    api_key = os.environ.get("UNION_API_KEY", os.environ.get("FLYTE_API_KEY", ""))
    kwargs: dict = {
        "endpoint": cfg.control_plane_url,
        "project": cfg.test_project,
        "domain": "development",
    }
    if org:
        kwargs["org"] = org
    if api_key:
        kwargs["api_key"] = api_key
    await flyte.init.aio(**kwargs)


async def hydrate_credentials() -> None:
    """Populate AWS + UNION env vars from the bootstrap secret, if needed.

    Call this at the top of any task that shells out to aws/gcloud/uctl.
    - Local run: ambient AWS_ACCESS_KEY_ID already set → no-op.
    - Remote run: the SDK has injected ``_E2E_CREDS_B64``; decode and export.

    Deliberately not a ``@flyte.trace``: hydrating is idempotent env-var
    manipulation with no return value — memoization would only serve to
    persist credential material in the trace store.
    """
    if os.environ.get("AWS_ACCESS_KEY_ID"):
        return
    blob = os.environ.get(E2E_CREDS_ENV_VAR)
    if not blob:
        raise RuntimeError(
            f"No AWS credentials: neither ambient env nor {E2E_CREDS_SECRET_KEY!r} "
            f"secret ({E2E_CREDS_ENV_VAR}). Run `flyte run --local "
            f"selfmanaged_common.py stash_credentials` first."
        )
    creds = json.loads(base64.b64decode(blob))
    os.environ["AWS_ACCESS_KEY_ID"] = creds["AKID"]
    os.environ["AWS_SECRET_ACCESS_KEY"] = creds["SAK"]
    if creds.get("TOK"):
        os.environ["AWS_SESSION_TOKEN"] = creds["TOK"]
    if creds.get("UAK"):
        os.environ["UNION_API_KEY"] = creds["UAK"]


