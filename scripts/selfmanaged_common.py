@flyte.trace
async def create_eager_api_key(cfg: BaseConfig) -> None:
    """Create the EAGER_API_KEY used by eager mode. Idempotent."""
    await init_union_client(cfg)
    try:
        await ApiKey.create.aio(name="EAGER_API_KEY")
        logger.info("EAGER_API_KEY created.")
    except Exception as e:
        # No typed AlreadyExists in the current SDK; re-creation surfaces as
        # a raw error. Safe to treat as pre-existing on reruns.
        logger.info(f"EAGER_API_KEY not created (likely exists): {e}")


@flyte.trace
async def create_cluster_pool(cfg: BaseConfig, org_name: str, pool_name: str) -> None:
    """`uctl create clusterpool` (no Python API yet in flyteplugins-union)."""
    org_flag = f"--org {org_name}" if org_name else ""
    spec = tempfile.NamedTemporaryFile(
        mode="w", suffix=".yaml", delete=False, prefix="clusterpool-",
    )
    spec.write(f"clusterPool:\n  id:\n    name: {pool_name}\n")
    spec.close()
    try:
        await sh(
            f"uctl create clusterpool --clusterPoolSpecFile {spec.name} {org_flag}",
            check=False,
        )
    finally:
        os.unlink(spec.name)
    logger.info(f"Cluster pool '{pool_name}' created/verified.")


@flyte.trace
async def assign_cluster_to_pool(
    cfg: BaseConfig, org_name: str, pool_name: str
) -> None:
    """`uctl create clusterpoolassignment`. Idempotent via check=False."""
    org_flag = f"--org {org_name}" if org_name else ""
    await sh(
        f"uctl create clusterpoolassignment "
        f"--poolName {pool_name} --clusterName {cfg.cluster_name} {org_flag}",
        check=False,
    )
    logger.info(f"Cluster '{cfg.cluster_name}' assigned to pool '{pool_name}'.")


@flyte.trace
async def create_project(cfg: BaseConfig) -> str:
    """`flyte.remote.Project.create`. Idempotent — swallow AlreadyExists."""
    await init_union_client(cfg)
    try:
        await Project.create.aio(
            id=cfg.test_project,
            name=cfg.test_project,
            description=f"E2E test project for cluster {cfg.cluster_name}",
        )
        logger.info(f"Project '{cfg.test_project}' created.")
    except Exception as e:
        logger.info(f"Project '{cfg.test_project}' not created (likely exists): {e}")
    return cfg.test_project


@flyte.trace
async def route_project_to_pool(
    cfg: BaseConfig, org_name: str, pool_name: str, domain: str
) -> None:
    """`uctl --logger.level 6 update cluster-pool-attributes` for one (project, domain, pool)."""
    org_flag = f"--org {org_name}" if org_name else ""
    attr = tempfile.NamedTemporaryFile(
        mode="w", suffix=".yaml", delete=False, prefix=f"cpa-{domain}-",
    )
    attr.write(
        f"domain: {domain}\n"
        f"project: {cfg.test_project}\n"
        f"clusterPoolName: {pool_name}\n"
    )
    attr.close()
    try:
        say(f"uctl update cluster-pool-attributes --force --attrFile {attr.name} {org_flag}")
        say(f"org: {org_name}\n"        
            f"domain: {domain}\n"
                    f"project: {cfg.test_project}\n"
                    f"clusterPoolName: {pool_name}\n"
                    )
        await sh(
            f"uctl update cluster-pool-attributes --force --attrFile {attr.name} {org_flag}",
            check=False,
        )
    finally:
        os.unlink(attr.name)


@flyte.trace
async def create_test_project_and_route(cfg: BaseConfig, org_name: str) -> str:
    """End-to-end: pool + assignment + project + per-domain routing.

    Returns the project name on success.
    """
    pool_name = cfg.cluster_name
    say(f"create_test_project_and_route: pool={pool_name} project={cfg.test_project} org={org_name}")
    await create_cluster_pool(cfg, org_name, pool_name)
    await assign_cluster_to_pool(cfg, org_name, pool_name)
    project = await create_project(cfg)
    for domain in ("development", "staging", "production"):
        await route_project_to_pool(cfg, org_name, pool_name, domain)
    say(f"create_test_project_and_route: project '{project}' routed to pool '{pool_name}' (dev/staging/prod)")
    return project


