@dataclass
class BaseConfig:
    """Cloud-agnostic knobs. Subclassed by AWSConfig / GCPConfig."""

    control_plane_url: str = ""
    cluster_name: str = ""

    helm_namespace: str = "union"
    helm_release_name: str = "union"
    helm_chart_repo: str = "https://github.com/unionai/helm-charts.git"
    # Empty = use the published unionai/dataplane chart from the repo.
    # Override via --helm_chart_branch if you want to test a WIP branch.
    helm_chart_branch: str = ""
    helm_chart_path: str = "charts/dataplane"
    helm_values_override: str = ""  # extra values file (e.g. "values-legacy.yaml" from the chart)
    dataplane_image_sha: str = ""  # if set, passed as --set image.union.tag=<sha>

    cluster_healthy_timeout: int = 300
    validate_timeout: int = 300

    skip_teardown: bool = False

    # Project to route test executions through. Defaults to cluster_name.
    test_project: str = ""

    def __post_init__(self):
        if not self.test_project:
            self.test_project = self.cluster_name


@dataclass(frozen=True)
class DataplaneValues:
    """Output of the ``provision_dataplane`` step."""

    yaml_text: str   # YAML generated by `uctl selfserve provision-dataplane-resources`
    org_name: str    # parsed from the same uctl output table


@dataclass(frozen=True)
class HelmRelease:
    name: str
    namespace: str


@dataclass
class TestResult:
    name: str
    passed: bool = False
    error: str = ""


@dataclass
class E2EResult:
    overall: str = ""  # "PASSED" | "FAILED"
    error: str = ""
    example_run_name: str = ""
    teardown_result: str = ""
    test_results: list[TestResult] = field(default_factory=list)

    @property
    def passed(self) -> bool:
        return self.overall == "PASSED"

    def summary(self) -> str:
        lines = [f"E2E Result: {self.overall}"]
        if self.error:
            lines.append(f"  Error: {self.error}")
        if self.example_run_name:
            lines.append(f"  Example run: {self.example_run_name}")
        if self.test_results:
            passed = sum(1 for t in self.test_results if t.passed)
            lines.append(f"  Verification: {passed}/{len(self.test_results)} passed")
            for t in self.test_results:
                status = "PASSED" if t.passed else "FAILED"
                line = f"    {t.name:<40} {status}"
                if t.error:
                    line += f"  {t.error[:80]}"
                lines.append(line)
        if self.teardown_result:
            lines.append(f"  Teardown: {self.teardown_result}")
        return "\n".join(lines)


