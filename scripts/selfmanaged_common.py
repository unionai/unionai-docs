@flyte.trace
async def resolve_chart_ref(cfg: BaseConfig) -> str:
    """Return a chart ref for helm (local path from git branch, or repo alias)."""
    if cfg.helm_chart_branch:
        say(f"resolve_chart_ref: cloning {cfg.helm_chart_repo}@{cfg.helm_chart_branch}")
        clone_dir = tempfile.mkdtemp(prefix="union-e2e-helm-")
        await sh(
            f"git clone --depth 1 --branch {cfg.helm_chart_branch} "
            f"{cfg.helm_chart_repo} {clone_dir}"
        )
        chart_ref = f"{clone_dir}/{cfg.helm_chart_path}"
        await sh(f"helm dependency update {chart_ref}", check=False)
        say(f"resolve_chart_ref: using local chart at {chart_ref}")
        return chart_ref
    say("resolve_chart_ref: using published unionai/dataplane")
    await sh(
        "helm repo add unionai https://unionai.github.io/helm-charts/ --force-update",
        check=False,
    )
    # Only refresh the unionai repo — `helm repo update` (no args) fails if
    # any unrelated repo on the local helm config is unreachable.
    await sh("helm repo update unionai")
    return "unionai/dataplane"


async def _watch_pods(namespace: str, stop: asyncio.Event, interval: int = 15) -> None:
    """Periodically dump `kubectl get pods -A` to the log until stopped.

    Untraced on purpose — this is diagnostic polling; each tick as a trace
    record would bloat the action store for no benefit.
    """
    while not stop.is_set():
        try:
            proc = await asyncio.create_subprocess_shell(
                "kubectl get pods -A --field-selector=metadata.namespace!=kube-system",
                stdout=asyncio.subprocess.PIPE,
                stderr=asyncio.subprocess.PIPE,
            )
            out, _ = await proc.communicate()
            text = (out or b"").decode(errors="replace").rstrip()
            if text:
                say("[pods] ↓\n" + text)
        except Exception as e:
            say(f"[pods] watcher error: {e}")
        try:
            await asyncio.wait_for(stop.wait(), timeout=interval)
        except asyncio.TimeoutError:
            pass


@flyte.trace
async def helm_install(cfg: BaseConfig, values_yaml: str, chart_ref: str) -> HelmRelease:
    """Materialize values YAML to a temp file and `helm upgrade --install`."""
    tmp = tempfile.NamedTemporaryFile(
        mode="w", suffix=".yaml", delete=False, prefix="union-e2e-values-",
    )
    tmp.write(values_yaml)
    tmp.close()
    values_file = tmp.name
    say(f"helm_install: values file at {values_file}, chart_ref={chart_ref}")

    override_flag = ""
    if cfg.helm_values_override:
        override_path = cfg.helm_values_override
        if not os.path.isabs(override_path) and "/" not in override_path and os.path.isdir(chart_ref):
            candidate = os.path.join(chart_ref, override_path)
            if os.path.exists(candidate):
                override_path = candidate
        override_flag = f'-f "{override_path}" '
        logger.info(f"  Using values override: {override_path}")

    image_tag_flag = ""
    if cfg.dataplane_image_sha:
        image_tag_flag = f"--set image.union.tag={cfg.dataplane_image_sha} "
        logger.info(f"  Overriding dataplane image tag: {cfg.dataplane_image_sha}")

    # ``--take-ownership`` (Helm 3.17+) lets an install/upgrade adopt existing
    # cluster resources (CRDs, namespaces) that lack Helm's owner annotations.
    # Without it, reruns after a partial install fail with "invalid ownership
    # metadata" on things like the knative-operator CRDs.
    say(f"helm_install: running `helm upgrade --install {cfg.helm_release_name}` "
        f"(this may take several minutes while --wait blocks on ready pods)...")

    stop = asyncio.Event()
    watcher = asyncio.create_task(_watch_pods(cfg.helm_namespace, stop))
    try:
        await sh(
            f"helm upgrade --install {cfg.helm_release_name} {chart_ref} "
            f"{override_flag}{image_tag_flag}-f \"{values_file}\" "
            f"-n {cfg.helm_namespace} --create-namespace --wait --timeout 10m "
            f"--force-conflicts --take-ownership"
        )
    finally:
        stop.set()
        try:
            await asyncio.wait_for(watcher, timeout=5)
        except asyncio.TimeoutError:
            watcher.cancel()

    say(f"helm_install: installed release '{cfg.helm_release_name}' in ns '{cfg.helm_namespace}'")
    return HelmRelease(name=cfg.helm_release_name, namespace=cfg.helm_namespace)


@flyte.trace
async def helm_uninstall(cfg: BaseConfig) -> list[str]:
    """Uninstall the release if it exists. Returns list of error messages."""
    errors: list[str] = []
    if await sh_ok(f"helm status {cfg.helm_release_name} -n {cfg.helm_namespace}"):
        logger.info(f"Uninstalling Helm release {cfg.helm_release_name}...")
        if not await sh_ok(
            f"helm uninstall {cfg.helm_release_name} -n {cfg.helm_namespace} "
            f"--wait --timeout 5m"
        ):
            errors.append("Helm uninstall failed")
    return errors


