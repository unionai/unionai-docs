# Phase 1 — Dataplane provisioning (uctl; shell out because no Python API yet)
# ============================================================================


@flyte.trace
async def provision_dataplane(cfg: BaseConfig, provider: str) -> DataplaneValues:
    """Run `uctl selfserve provision-dataplane-resources`.

    Returns the generated YAML text and the parsed org name. The YAML is not
    written anywhere persistent; callers materialize it to a temp file before
    passing to helm.
    """
    assert cfg.control_plane_url, "control_plane_url is required"
    assert cfg.cluster_name, "cluster_name is required"

    work_dir = tempfile.mkdtemp(prefix="union-e2e-provision-")
    say(f"provision_dataplane: running uctl selfserve on {cfg.control_plane_url} "
        f"(cluster={cfg.cluster_name}, provider={provider})")

    # ``hydrate_credentials`` has already populated UNION_API_KEY (either from
    # the ambient shell or the injected secret), so uctl config init runs
    # non-interactively and does not need a TTY.
    await sh(f"cd {work_dir} && uctl config init --host={cfg.control_plane_url}")
    output = await sh(
        f"cd {work_dir} && "
        f"uctl selfserve provision-dataplane-resources "
        f"--clusterName {cfg.cluster_name} --provider {provider}"
    )

    # Find the generated values file in the temp dir.
    values_file = ""
    for line in output.splitlines():
        if "Generated" in line and ".yaml" in line:
            for word in line.split():
                if word.endswith(".yaml"):
                    values_file = os.path.join(work_dir, word)
                    break
            if values_file:
                break
    if not values_file or not os.path.exists(values_file):
        import glob
        candidates = glob.glob(os.path.join(work_dir, "*-values.yaml"))
        if candidates:
            values_file = candidates[0]
    if not values_file or not os.path.exists(values_file):
        raise RuntimeError(
            f"Could not find generated values file in {work_dir}. uctl output:\n{output}"
        )

    with open(values_file) as f:
        yaml_text = f.read()
    say(f"provision_dataplane: got values YAML ({len(yaml_text)} chars) from {values_file}")

    # Parse org name from the uctl output table (first table row matching
    # cluster_name contains [_, org, ...] between pipes).
    org_name = ""
    for line in output.splitlines():
        if cfg.cluster_name in line and "|" in line:
            fields = [f.strip() for f in line.split("|")]
            if len(fields) >= 3 and fields[1]:
                org_name = fields[1]
                break
    if org_name:
        say(f"provision_dataplane: org={org_name}")

    return DataplaneValues(yaml_text=yaml_text, org_name=org_name)


